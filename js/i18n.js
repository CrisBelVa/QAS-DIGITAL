let currentLang = 'pt';

function setLang(lang) {
    if (!T[lang]) return;
    currentLang = lang;
    try { localStorage.setItem('qas-lang', lang); } catch (e) { }
    document.documentElement.lang = lang;
    document.title = T[lang]['page.title'] || 'QAS-Digital';
    
    SMAP.forEach(([sel, key, type]) => {
        const v = T[lang][key];
        if (v === undefined) return;
        document.querySelectorAll(sel).forEach(el => {
            if (type === 'html') el.innerHTML = v;
            else el.textContent = v;
        });
    });

    document.querySelectorAll('[data-lang]').forEach(b =>
        b.classList.toggle('lang-active', b.dataset.lang === lang)
    );
}

function initLang() {
    let lang = 'pt';
    try { lang = localStorage.getItem('qas-lang') || 'pt'; } catch (e) { }
    setLang(T[lang] ? lang : 'pt');
}

window.addEventListener('DOMContentLoaded', initLang);
